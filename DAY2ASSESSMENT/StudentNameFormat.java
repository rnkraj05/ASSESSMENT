//Step 1: Create a public class of Stdent name format.
public class StudentNameFormat {
    public static void main(String[] args) {

        //Step 2: Assign the students name using string method.
        String name = "Raunak Raj";

        //Step 3: Display the Original Name.
        System.out.println("Original Name: " + name);

        //Step 3: Display the Uppercase of student name using uppercase method.
        System.out.println("Uppercase: " + name.toUpperCase());

        //Step 4: Display the Lowercase of student name using lowercase method.
        System.out.println("Lowercase: " + name.toLowerCase());

        //Step 5: Display the Length of student name using length method.
        System.out.println("Length of Name: " + name.length());
    }
}